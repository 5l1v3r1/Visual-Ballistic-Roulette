linear_reg <- function(input) {
  revs_a = 1:length(input)
  lm1_t = lm(input ~ revs_a)
  speeds_a = 3.14*0.65/input
  lm1_s = lm(speeds_a ~ revs_a)
  list(lm1_t, lm1_s)
}

